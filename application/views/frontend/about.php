                        <script>
                        document.getElementById('aboutContactForm').addEventListener('submit', function(e) {
                            e.preventDefault();
                            var fname   = this.querySelector('[name="fname"]').value.trim();
                            var lname   = this.querySelector('[name="lname"]').value.trim();
                            var email   = this.querySelector('[name="email"]').value.trim();
                            var phone   = this.querySelector('[name="phone"]').value.trim();
                            var message = this.querySelector('[name="message"]').value.trim();
                            var resp = document.getElementById('about-contact-response');
                            var btn  = document.getElementById('about-contact-btn');

                            if (!fname || !lname || !email || !phone) {
                                resp.innerHTML = '<div class="about-contact-alert about-contact-alert-error"><i class="fa-solid fa-circle-exclamation"></i> Please fill in all required fields.</div>';
                                return;
                            }
                            if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
                                resp.innerHTML = '<div class="about-contact-alert about-contact-alert-error"><i class="fa-solid fa-circle-exclamation"></i> Please enter a valid email address.</div>';
                                return;
                            }

                            btn.disabled = true;
                            btn.querySelector('span').textContent = 'Sending...';
                            resp.innerHTML = '';

                            fetch('<?php echo site_url('contact/save'); ?>', {
                                method: 'POST',
                                headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
                                body: new URLSearchParams({ name: fname + ' ' + lname, email: email, phone: phone, message: message }).toString()
                            })
                            .then(function(r) { return r.json(); })
                            .then(function(data) {
                                if (data.success) {
                                    resp.innerHTML = '<div class="about-contact-alert about-contact-alert-success"><i class="fa-solid fa-circle-check"></i> ' + data.message + '</div>';
                                    document.getElementById('aboutContactForm').reset();
                                } else {
                                    resp.innerHTML = '<div class="about-contact-alert about-contact-alert-error"><i class="fa-solid fa-circle-exclamation"></i> ' + (data.message || 'Something went wrong.') + '</div>';
                                }
                            })
                            .catch(function() {
                                resp.innerHTML = '<div class="about-contact-alert about-contact-alert-error"><i class="fa-solid fa-circle-exclamation"></i> Network error. Please try again.</div>';
                            })
                            .finally(function() {
                                btn.disabled = false;
                                btn.querySelector('span').textContent = 'Send Message';
                            });
                        });
                        </script>
